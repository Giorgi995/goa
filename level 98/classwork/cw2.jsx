function App() {
  const objectStyle = {
    backgroundColor: "green",
    color: "green",
    padding: "15px",
    marginTop: "20px"
  };

  return (
    <div>
      {/* Inline styling */}
      <h1
        style={{
          backgroundColor: "green",
          color: "green",
          padding: "15px"
        }}
      >
        Inline Styling
      </h1>

      {/* Object variable styling */}
      <h1 style={objectStyle}>
        Object Variable Styling
      </h1>
    </div>
  );
}

export default App;
